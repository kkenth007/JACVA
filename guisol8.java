    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        //สามเหลี่ยมทั่วไป
        try {
            String a =txt1.getText();
        double base =Double.parseDouble(a);
        String b=txt2.getText();
        double hight =Double.parseDouble(b);
        double sum=(0.5*(base*hight));
        txt3.setText(""+sum);
        //สามเหลี่ยมด้านเท่า
        String c =txt4.getText();
        double corner =Double.parseDouble(c);
        double sumcornner = (Math.sqrt(3)/4)*corner*corner;
        txt5.setText(""+sumcornner);
        //สามเหลี่ยมหน้าจั่ว
        String d=txt6.getText();
        double base1 = Double.parseDouble(d);
        String e= txt7.getText();
        double corner1= Double.parseDouble(e);
        double sum3=((base1/4)*Math.sqrt(4*(corner1*corner1)-(base1*base1)));
        txt8.setText(""+sum3);
        //สามเหลี่ยมมุมฉาก
        String f=txt9.getText();
        double corner2= Double.parseDouble(f);
        double sum4=(0.5*corner2*corner2);
        txt10.setText(""+sum4);
        }
          catch (Exception e) {
          JOptionPane.showMessageDialog(null,"คุณต้องกรอกตัวเลขไห้ครบทุกช่อง\nด้วยน่ะครับ");
          }
    }//GEN-LAST:event_btn1ActionPerformed

